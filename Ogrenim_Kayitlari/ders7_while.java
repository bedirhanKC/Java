package intro;

public class ders7_while {

	public static void main(String[] args) {

		int i=0; 
		
		while(i<=10) { // parantezin içini sağladığı sürece döngü dönmeye devam eder
			
			System.out.println(i);
			i++;
			
		}
		
		System.out.println("while döngüsü tamamlandi");
		
	}

}
