package intro;

public class ders8_do_while {

	public static void main(String[] args) {

		int j=100;
		
		do {
		
			System.out.println(j);
			
		}while(j<10); //do noktasından döngü bir kere çalışır ve parantezin içi sağlamaya devam ettiği sürece döngü devam eder
		
		System.out.println("Do while döngüsü sona erdi.");
		
	}

}




